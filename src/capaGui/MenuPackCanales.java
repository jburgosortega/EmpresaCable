    private void btnMostratActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostratActionPerformed
       this.cmbCanales.removeAllItems();
        
        if (this.cmbPackElegir.getSelectedIndex() != 0) {
            NegocioProductos neg = new NegocioProductos();

            DatosProductos acad = neg.buscarDatosPackCanales((this.cmbPackElegir.getSelectedIndex()));

            this.txtNombre.setText(acad.getNombrePackCanales());
            this.txtTipo.setText(acad.getTipoPackCanales());
            this.txtPrecio.setText("" + acad.getPrecioPackCanales());
            ////////////////////////////////////////////////////////////////////////
            llenarCanal();
            ////////////////////////////////////////////////////////////////////////           

        } else {
            JOptionPane.showMessageDialog(this, "Seleccione Pack de Canales",
                    "Error de Ingreso", JOptionPane.ERROR_MESSAGE);
            this.cmbPackElegir.requestFocus();
        }
    }//GEN-LAST:event_btnMostratActionPerformed

