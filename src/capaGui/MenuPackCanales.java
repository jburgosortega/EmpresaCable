    private void llenarNombre() {
        NegocioProductos neg2 = new NegocioProductos();
        ArrayList<DatosProductos> lista2 = neg2.getDatos();
        for (int i = 0; i < lista2.size(); i++) {
            DatosProductos sv = lista2.get(i);

            this.cmbPackElegir.addItem(sv.getNombre());
        }
    }

    private void llenarCanal() {
        NegocioProductos neg = new NegocioProductos();
        ArrayList<DatosProductos> listaCanal = neg.getDatosCanal();
        for (int i = 0; i < listaCanal.size(); i++) {
            DatosProductos sv = listaCanal.get(i);
            if (this.cmbPackElegir.getSelectedIndex() == sv.getIdCanal()) {
                this.cmbCanales.addItem(sv.getNombreCanal());
            }
        }
    }
